package sec4;

public class Printer {
	static public void println(int data){
		System.out.println(data);
	}
	static public void println(boolean data){
		System.out.println(data);
	}
	static public void println(double data){
		System.out.println(data);
	}
	static public void println(String data){
		System.out.println(data);
	}
	
}	
